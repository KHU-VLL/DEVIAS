def main(args, ds_init):
    utils.init_distributed_mode(args)

    if ds_init is not None:
        utils.create_ds_config(args)

    print(args)

    device = torch.device(args.device)

    # fix the seed for reproducibility
    seed = args.seed + utils.get_rank()
    torch.manual_seed(seed)
    np.random.seed(seed)
    random.seed(seed)
    torch.cuda.manual_seed(seed)
    torch.backends.cudnn.deterministic = True

    cudnn.benchmark = True

    #! hvu_eval
    dataset_list = build_dataset(is_train=False, test_mode=False, args=args) 
    # dataset_seen_split, dataset_unseen_split
    
    sampler_list = []
    for dataset in dataset_list:
        sampler_list.append(torch.utils.data.SequentialSampler(dataset))

    data_loader_val = []
    for dataset,sampler in zip (dataset_list, sampler_list):
        data_loader_val.append(torch.utils.data.DataLoader(
            dataset, sampler=sampler,
            batch_size=int(1.5 * args.batch_size),
            num_workers=args.num_workers,
            pin_memory=args.pin_mem,
            drop_last=False
        ))

    model = create_model(
        args.model,
        pretrained=False,
        num_classes=HVU_NUM_ACTION_CLASSES,
        all_frames=args.num_frames * args.num_segments,
        tubelet_size=args.tubelet_size,
        fc_drop_rate=args.fc_drop_rate,
        drop_rate=args.drop,
        drop_path_rate=args.drop_path,
        attn_drop_rate=args.attn_drop_rate,
        drop_block_rate=None,
        use_checkpoint=args.use_checkpoint,
        init_scale=args.init_scale,
        num_latents=args.num_latents,
        head_type=args.head_type,
        slot_matching_method=args.slot_matching_method,
        agg_weights_tie=args.agg_weights_tie,
        agg_depth=args.agg_depth,
        num_scene_classes=HVU_NUM_SCENE_CLASSES
    )

    #! load checkpoint already trained on HVU train set
    checkpoint = torch.load(args.finetune, map_location='cpu')
    msg = model.load_state_dict(checkpoint['module'])
    print(msg)

    model.to(device)
    model_without_ddp = model
    n_parameters = sum(p.numel() for p in model.parameters() if p.requires_grad)

    print("Model = %s" % str(model_without_ddp))
    print('number of params:', n_parameters)

    assigner = None
    skip_weight_decay_list = model.no_weight_decay()
    print("Skip weight decay list: ", skip_weight_decay_list)

    if args.enable_deepspeed:
        loss_scaler = None
        optimizer_params = get_parameter_groups(
            model, args.weight_decay, skip_weight_decay_list,
            assigner.get_layer_id if assigner is not None else None,
            assigner.get_scale if assigner is not None else None,
            agg_block_scale = args.agg_block_scale
            )
        model, optimizer, _, _ = ds_init(
            args=args, model=model, model_parameters=optimizer_params, dist_init_required=not args.distributed,
        )
        print("model.gradient_accumulation_steps() = %d" % model.gradient_accumulation_steps())
        assert model.gradient_accumulation_steps() == args.update_freq

    else:
        if args.distributed:
            model = torch.nn.parallel.DistributedDataParallel(model, device_ids=[args.gpu], find_unused_parameters=True)
            model_without_ddp = model.module

        optimizer = create_optimizer(
            args, model_without_ddp, skip_list=skip_weight_decay_list,
            get_num_layer=assigner.get_layer_id if assigner is not None else None, 
            get_layer_scale=assigner.get_scale if assigner is not None else None)
        loss_scaler = NativeScaler()

    validation_action(data_loader_val[0], model, device, header = 'Seen Action VAL:')
    validation_action(data_loader_val[1], model, device, header = 'Unseen Action VAL:')
    validation_scene(data_loader_val[0], model, device, header = 'Seen Scene VAL:')
    validation_scene(data_loader_val[1], model, device, header = 'Unseen Action VAL:')

   
if __name__ == '__main__':
    opts, ds_init = get_args()
    if opts.output_dir:
        Path(opts.output_dir).mkdir(parents=True, exist_ok=True)
    main(opts, ds_init)
